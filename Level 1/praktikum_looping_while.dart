// Program Dart untuk melakukan iterasi dengan menggunakan while
void main() {
  // Inisialisasi total
  int total = 0;

  // Loop while untuk iterasi selama total kurang dari atau sama dengan 10
  while (total <= 10) {
    // Mencetak pesan ke konsol dengan nomor iterasi
    print("iterasi ke-$total");

    // Increment total untuk iterasi berikutnya
    total++;
  }
}
